package operadores.logico.relacional.condicional.java;

public class QuintaClasse {

	public static void main(String[] args) {
		for (int numero = 0; numero <= 10; numero++) {
			if(numero == 7) {
				System.out.println("Procurando o Numero 7...");
				System.out.println("Achei o Numero 7");
				System.out.println("Finalizando a Execução ...");
				break;
			}
		}
	}

}
